#\n for a new line in string
a = 'maya eats apples \nbut apples dont eat maya'
print(a)

#for adding quotes to be printed in string use \' or \""
b = 'maya eats apples \nbut apples dont \'eat\' maya'
print(b)

c = 'maya eats apples \nbut apples dont \teat\t maya'
print(c)


d = 'maya eats apples \bbut apples dont eat maya'
print(d)


